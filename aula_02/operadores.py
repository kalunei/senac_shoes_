# Operadores de comparação ---------------------------------------------------------------

'''
==      igual a. 
!=      diferente de.
>       maior que.
>=      maior ou igual.
<       menor que.
<=      menor ou igual.
'''
numero = 101
outro_numero = 20 

print(f"{numero} > {outro_numero} = {numero > outro_numero}")
print(f"{numero} < {outro_numero} = {numero < outro_numero}")
print(f"{numero} == {outro_numero} = {numero == outro_numero}")
print(f"{numero} != {outro_numero} = {numero != outro_numero}")
print(f"{numero} >= {outro_numero} = {numero >= outro_numero}")
print(f"{numero} <= {outro_numero} = {numero <= outro_numero}")



